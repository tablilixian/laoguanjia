changes = []
for poem in data:
    title = poem.get('title_cns', '')
    current_grade = poem.get('grade', 0)
    
    new_grade = None
    
    if title in grade_mapping:
        new_grade = grade_mapping[title]
    else:
        for key, val in grade_mapping.items():
            if key in title or title in key:
                new_grade = val
                break
    
    if new_grade is not None and new_grade != current_grade:
        changes.append({
            'id': poem.get('id'),
            'title': title,
            'author': poem.get('author_cns'),
            'old_grade': current_grade,
            'new_grade': new_grade
        })
        poem['grade'] = new_grade

print(f"共发现 {len(changes)} 处需要修改")

from collections import defaultdict
by_old = defaultdict(list)
for c in changes:
    by_old[c['old_grade']].append(c)

for old_g in sorted(by_old.keys()):
    print(f"\n=== 从 grade {old_g} 调整的 ({len(by_old[old_g])} 首) ===")
    for c in by_old[old_g][:5]:
        print(f"  {c['title']} - {c['author']}: {c['old_grade']} -> {c['new_grade']}")
    if len(by_old[old_g]) > 5:
        print(f"  ... 还有 {len(by_old[old_g])-5} 首")

